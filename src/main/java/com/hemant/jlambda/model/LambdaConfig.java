package com.hemant.jlambda.model;

public class LambdaConfig {
    private String name;

    public LambdaConfig(String name) {
        this.name = name;
    }

    public LambdaConfig() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
